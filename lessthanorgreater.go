package main
import "fmt"
func main()  {
	var a, b int = 10, 5
	fmt.Println(a<b) //using lessthan operator
	fmt.Println(a>b) //using greaterthan operator
	fmt.Println(a<=b) //using lessthan or equalto 
	fmt.Println(a>=b) //using greater than or equal to
	fmt.Println(a==b) //using equal to
	fmt.Println(a!=b) // using not equal to 
}
